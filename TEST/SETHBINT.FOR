        PROGRAM SETHBINT
C ENABLES ON-BOARD INTERRUPT BY SETTING BIT 5 OF STATUS HIGH #20
C WITH NO RESETS  #1F
C #20 + #1F = #3F
C STATUS OFFSET = #01
        INTEGER*2 SGBASE,STA,INTB,IBYTE
        PARAMETER (SGBASE=#02A0,STA=#01,INTB=#3F)
C GET INITIAL VALUE OF STATUS BYTE
        CALL FETCH(SGBASE+STA,IBYTE)
        PRINT *, IBYTE
        CALL OUTPUT(SGBASE+STA,INTB)
C GET FINAL VALUE OF STATUS BYTE
        CALL FETCH(SGBASE+STA,IBYTE)
        PRINT *, IBYTE
        END
c ***********************  HZ_200 *************************
        SUBROUTINE HZ_200
        INTEGER*2 SGBASE,RES
        PARAMETER (SGBASE=#02A0,RES=#01)
        CALL OUTPUT(SGBASE+RES,INT2(#0F)) !RESET HB
        CALL OUTPUT(SGBASE+RES,INT2(#3F)) !ENABLE+RESET HB
        RETURN
        END
